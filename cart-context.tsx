// ─── Helper functions ─────────────────────────────────────────

export const CATEGORIES = [...new Set(PRODUCTS.map(p => p.category))].sort();
export const BRANDS = [...new Set(PRODUCTS.map(p => p.brand))].sort();

export function getProduct(id: string): Product | undefined {
  return PRODUCTS.find(p => p.id === id);
}

export function getRelatedProducts(product: Product, count = 4): Product[] {
  return PRODUCTS
    .filter(p => p.id !== product.id && (p.category === product.category || p.brand === product.brand))
    .slice(0, count);
}

export function getFeaturedProducts(): Product[] {
  return PRODUCTS.filter(p => p.featured);
}

export function searchProducts(query: string): Product[] {
  const q = query.toLowerCase();
  return PRODUCTS.filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.brand.toLowerCase().includes(q) ||
    p.category.toLowerCase().includes(q) ||
    p.shortDesc.toLowerCase().includes(q) ||
    p.tags.some(t => t.includes(q))
  );
}

export const getBadgeStyle = (color?: string) => {
  const map: Record<string, string> = {
    aurora:  "bg-aurora/20 text-aurora border-aurora/30",
    electric:"bg-electric/20 text-electric border-electric/30",
    plasma:  "bg-plasma/20 text-plasma border-plasma/30",
    ember:   "bg-ember/20 text-ember border-ember/30",
  };
  return (map[color || "electric"] || map.electric) + " border";
};

export const formatPrice = (egp: number) =>
  new Intl.NumberFormat("ar-EG", { style: "currency", currency: "EGP", minimumFractionDigits: 0 }).format(egp);
